# Isto é uma Variavel, é um nome dado ao local da memória que contém o valor
a = 10

# ela é feita para armazenar algo(valor, texto, qualquer coisa) e pode ser chamada multiplas vezes
print(a)
print(a)
print(a)
print(a)
print(a)

# No Lugar de Espaço é Colocado _ (UnderLine)
# Para ser uma Variavel ela tem que ter: Nome da Vairvel, sinal de igual(=) para igualala a algo, e o dado que ela comporta
minha_variavel = 1

# Dependendo do que a variavel armazena, ela vai ser de algum tipo
# Variavel Tipo Int
minha_variavel_1 = 10

# Variavel Tipo Float
minha_variavel_2 = 1.4

# Variavel Tipo String (Texto)
minha_variavel_3 = "Salve Garai"

# Variavel Tipo Booleana(True / False) V ou F
minha_variavel_4 = True


print(minha_variavel_1, minha_variavel_2, minha_variavel_3, minha_variavel_4) #Printando assim vai ficar 1 do lado da outra

# Se a Varivel for Igualada a Outro valor, ela então vai assumir o ultimo valor que ela foi igualada
minha_variavel_1 = 2330
minha_variavel_2 = 80.76
minha_variavel_3 = "Eu Mudei Gente"
minha_variavel_4 = False

print(minha_variavel_1)
print(minha_variavel_2)
print(minha_variavel_3)
print(minha_variavel_4)