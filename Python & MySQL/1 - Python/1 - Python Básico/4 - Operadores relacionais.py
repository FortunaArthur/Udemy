X = 10
Y = 30

# Os Operadores Relacionais são feitos para afzer comparações
# Operador de Igualdade
print(X == Y) # X IGUAL A Y

# Operador de 
print(X != Y) # X IGUAL A Y

# Operador de 
print(X > Y) # X MAIOR Q Y

# Operador de 
print(X < Y) # X MENOR Q Y

# Operador de 
print(X >= Y) # X MAIOR OU IGUAL A Y

# Operador de 
print(X <= Y) # X MENOR OU IGUAL A Y

# Quando Compilar o programa vai retornar com verdadeiro ou falso (True or False) para as expressões acima